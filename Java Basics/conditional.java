
public class conditional{
    public static void main(String[] args){
        // switch statement

        String day = "Monday";

        switch (day) {
            case "Monday":
                System.out.println("Today is " + day);
                break;

            case "Tuesday":
                System.out.println("Today is " + day);
                break;

            case "Wednesday":
                System.out.println("Today is "+ day);
                break; 
            default:
                System.out.println("please enter valid input");           
        }

    
    }
}