
public class Array {

    public static void main(String[] args) {

        int marks[] = new int[4];
        marks[0] = 97;
        marks[1] = 80;
        marks[2] = 85;
        marks[3] = 82;

        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        };

        String Students[] = {"Nishant", "Manish", "Sahil", "Mitesh"};

        for (int i = 0; i < Students.length; i++) {
            System.out.println(Students[i]);
        }

    }
}
